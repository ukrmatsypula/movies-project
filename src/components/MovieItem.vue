<template>
  <div class="movie-item mb-3">
    <div class="movie-item__poster" :style="posterBg">
      <div class="movie-info-wrap">
        <div class="movie__item-info">
          <h3 class="movie__title">{{ movie.Title }}</h3>
          <span class="movie__year">{{ movie.Year }}</span>
        </div>
        <div class="movie-item__controls row no-gutters">
          <div class="col mr-2">
            <BButton size="md" block variant="outline-light">Edit</BButton>
          </div>
          <div class="col ml-2">
            <BButton
              size="md"
              block
              variant="outline-light"
              @click="emitRemoveEvent"
              >Remove</BButton
            >
          </div>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
export default {
  name: "MovieItem",
  props: {
    movie: {
      type: Object,
      required: true,
    },
  },
  computed: {
    posterBg() {
      return {
        "background-image": `url(${
          this.movie.Poster !== "N/A"
            ? this.movie.Poster
            : "https://via.placeholder.com/400"
        })`,
      };
    },
  },
  methods: {
    emitRemoveEvent() {
      this.$emit("removeItem", {
        id: this.movie.imdbID,
        title: this.movie.Title,
      });
    },
  },
};
</script>

